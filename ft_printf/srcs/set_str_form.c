#include "ft_printf.h"

char	*set_str_form(const char *str)
{
	int	i;

	i = 0;
	while (str[++i])
	{
		if (str[i] == 'c' || str[i] == 's' || str[i] == 'p' || \
				str[i] == 'd' || str[i] == 'i' || str[i] == 'u' || \
				str[i] == 'x' || str[i] == 'X' || str[i] == '%')
			break ;
	}
	return (ft_substr(str, 0, i + 1));
}
